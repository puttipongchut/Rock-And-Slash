    public boolean isSolid(int x, int y) {
        int col = x / TILE_SIZE;
        int row = y / TILE_SIZE;
        int[][] currentmapData = allmapData[currentLevel];

        if (col < 0 || col >= currentmapData[0].length || row < 0 || row >= currentmapData.length) {
            return false;
        }
        return currentmapData[row][col] == 1; 
    }
    
